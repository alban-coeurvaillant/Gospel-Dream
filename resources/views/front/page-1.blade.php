    <p>Le nom « Carotte » est un emprunt savant au latin Carotta, qui ne remonte qu'au xvie siècle, avec une première
        attestation en 15083. En français régional ou patois, la Carotte est également parfois appelée « Faux-chervis
        »4, « Girouille »4, « Racine »3, « Racine jaune »3, « Pastonade »3 (et ses dérivés « Pastounade », « Ponstinade
        », « Pastanade »5, de « Panais », qui a aussi désigné la Carotte3). Le nom « Carotte » peut également
        s'appliquer à de nombreuses autres plantes, et a désigné aux xixe siècle et xxe siècle, surtout en Lyonnais,
        Dauphiné et Savoie, la Betterave3.</p>

    <p>Au xixe siècle, le nom seul Carotte se rattache tout autant à la sous-espèce Daucus carota subsp. sativus qu'à
        l'espèce Daucus carota elle-même6,7,8,9, ainsi qu'à la sous-espèce commutatus (Paol.) Thell., 192610,11, et à
        l'espèce Daucus muricatus (L.) L.12. Par conséquent le nom « Carotte cultivée » est celui recommandé pour Daucus
        carota subsp. sativus1,13. Le nom « Carotte potagère » s'appliquerait en revanche à l'espèce selon la Base de
        données des plantes vasculaires du Canada7.</p>
