
<p class="intro_press">GOSPEL DREAM : la voix sacrée du gospel</p>
<p class="article">Les chanteurs bougent, s’emportent, vivent, tapent 
des mains, marquent le tempo. Le secret de Gospel Dream ? Leur foi en 
Dieu, une foi indestructible. « Notre monde est fou, il est malade ; 
affirme Michel M’PASSY, prédicateur, chef de chœur, vous qui aimez le 
gospel, aidez-nous à transformer ce monde. Gospel signifie Evangile et 
le mot Evangile peut se résumer en un seul mot : Amour. Donnez de 
l’amour autour de vous ! » Le répertoire est parfois mélancolique tout 
comme les thèmes abordés : « nous vivons des temps insupportables et 
nous devons prier pour la paix ».</p>
<p class="press_name">Le journal de Saône et Loire	</p>
<div id="divider"></div>
<p class="intro_press">Plus de quatre cent auditeurs communient avec le gospel dans l’église Saint-Laurent.</p>
<p class="article">Le groupe ne se contente pas de faire de la musique, 
comme l’a expliqué au fil du spectacle, son chef de chœur, Michel 
M’PASSY. Tout en chantant des morceaux traditionnels du gospel et en les
  dansant , le groupe prêche à sa manière l’ Evangile, chacun de ses 
musiciens livrant par sa voix, son cœur et son corps, sa foi dans le 
Seigneur.  « Dans ce monde malade d’inégalités, d’injustices, de famines
 et de guerres, il ne faut pas baisser les bras. Tous ensemble, pour 
l’amour, nous pouvons changer les choses… » a dit le chef de chœur à 
l’auditoire.</p>
